Object.entries(allScenarios).forEach(([scenarioNum, messages]) => {
  const filePath = path.join(scenarioDir, `scenario_${scenarioNum}.json`);
  
  if (!fs.existsSync(filePath)) {
    console.log(`파일 없음: ${filePath}`);
    return;
  }
  
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let updated = 0;
  
  data.nation.forEach((n) => {
    const name = n[0];
    if (messages[name]) {
      n[4] = messages[name];
      updated++;
    }
  });
  
  if (updated > 0) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 4), 'utf8');
    console.log(`scenario_${scenarioNum}: ${updated}개 국가 임관 권유문 업데이트`);
  }
});

